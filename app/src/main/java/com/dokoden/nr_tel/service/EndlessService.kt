class EndlessService : LifecycleService() {
    private val viewModel by lazy {
        ViewModelProvider.AndroidViewModelFactory(application).create(MainViewModel::class.java)
    }

    private val accountViewModel by lazy {
        ViewModelProvider.AndroidViewModelFactory(application).create(AccountViewModel::class.java)
    }

    private val sipStack by lazy {
        ViewModelProvider.AndroidViewModelFactory(application).create(SipStackViewModel::class.java)
    }

    private var ringToneTimer: Timer? = null
    private var audioFocusRequest: AudioFocusRequest? = null
    private var audioFocusUsage = -1
    private var origVolumes = arrayOf(-1, -1, -1, -1)
    private var activeNetwork = ""

    private val telephonyManager by lazy { application.getSystemService(Context.TELEPHONY_SERVICE) as TelephonyManager }

    private lateinit var notifyManager: NotificationManager
    private lateinit var notifyBuilder: NotificationCompat.Builder
    private val defaultChannelID by lazy { "$packageName.channel_default" }
    private val callChannelID by lazy { "$packageName.channel_call" }
    private val notifyTitle by lazy { resources.getString(R.string.notification_title) }
    private val notifyText by lazy { resources.getString(R.string.notification_text) }

    private val audioManager by lazy { getSystemService(Context.AUDIO_SERVICE) as AudioManager }
    private val vibrator by lazy { getSystemService(Context.VIBRATOR_SERVICE) as Vibrator }
    private val ringTone by lazy {
        RingtoneManager.getActualDefaultRingtoneUri(applicationContext, RingtoneManager.TYPE_RINGTONE).let {
            RingtoneManager.getRingtone(applicationContext, it)
        }
    }

    private lateinit var partialWakeLock: PowerManager.WakeLock
    private val partialTag by lazy { "$packageName:partial_wakeTag" }
    private lateinit var proximityWakeLock: PowerManager.WakeLock
    private val proximityTag by lazy { "$packageName:proximity_wakeTag" }
    private lateinit var wifiLock: WifiManager.WifiLock
    private val wifiTag by lazy { "$packageName:wifi_tag" }

    private var isTransferReplace = true

    private var oldLIST = listOf<AccountData>()
    private var allAccountSize = 0

    override fun onBind(intent: Intent): IBinder? {
        super.onBind(intent)
        return null
    }

    override fun onCreate() {
        super.onCreate()
        sipStack.initLib()

        (getSystemService(Context.POWER_SERVICE) as PowerManager).also {
//            partialWakeLock = it.newWakeLock(PowerManager.PARTIAL_WAKE_LOCK, partialTag)
            partialWakeLock = it.newWakeLock(
                PowerManager.PARTIAL_WAKE_LOCK or PowerManager.ACQUIRE_CAUSES_WAKEUP, partialTag
            )
            proximityWakeLock = it.newWakeLock(PowerManager.PROXIMITY_SCREEN_OFF_WAKE_LOCK, proximityTag)
        }

        wifiLock = (applicationContext.getSystemService(Context.WIFI_SERVICE) as WifiManager)
            .createWifiLock(WifiManager.WIFI_MODE_FULL_HIGH_PERF, wifiTag)

        accountViewModel.enabledAccount.observe(this) { newLIST ->
            allAccountSize = newLIST.size
            (oldLIST - newLIST).forEach { sipStack.delAccount(it) }
            (newLIST - oldLIST).forEach { sipStack.addAccount(it) }
            oldLIST = newLIST

            val inboxStyle = NotificationCompat.InboxStyle()
            newLIST.forEach { accountData ->
                inboxStyle.addLine(accountData.titleText)
            }

            notifyBuilder.setStyle(inboxStyle)
            notifyManager.notify(Constants.DefaultNotifyID, notifyBuilder.build())
        }

        accountViewModel.outgoingAccount.observe(this) {

        }

        sipStack.currentCall.observe(this) {
            if (it.size == 1) {
                when (it[0].CallStatus) {
                    Constants.ObsCallActions.HangupCall.name -> declineCurrentCall()
                    Constants.ObsCallActions.OnIncomingCall.name -> {
                        viewModel.callName.postValue(it[0].callName)
                        viewModel.callNumber.postValue(it[0].callNumber)
                        incomingCall()
                    }
                    Constants.ObsCallActions.AcceptIncomingCall.name -> acceptIncomingCall()
                    Constants.ObsCallActions.DeclineIncomingCall.name -> declineIncomingCall()
                    Constants.ObsCallActions.OutgoingCall.name -> outgoingCall()
                    Constants.ObsCallActions.TransferCall.name -> transfer()
                }
            } else if (it.size > 1) {
                it.forEach { callStatusData ->
                    if (callStatusData.currentCall != 1) {
                        sipStack.hangupCall(callStatusData.callId)
                    }
                }
            }
        }

        sipStack.defaultAccount.observe(this) {

        }

        sipStack.registeredAccount.observe(this) {
            when (it.size) {
                0 -> {
                    notifyBuilder
                        .setSmallIcon(R.drawable.ic_indicator_cross)
                        .color = resources.getColor(R.color.colorDeclineBackground, theme)
                    notifyManager.notify(Constants.DefaultNotifyID, notifyBuilder.build())
                }
                allAccountSize -> {
                    notifyBuilder
                        .setSmallIcon(R.drawable.ic_indicator_circle)
                        .color = resources.getColor(R.color.colorLightGreen, theme)
                    notifyManager.notify(Constants.DefaultNotifyID, notifyBuilder.build())
                }
                in 1 until allAccountSize -> {
                    notifyBuilder
                        .setSmallIcon(R.drawable.ic_indicator_retry)
                        .color = resources.getColor(R.color.colorLightGreen, theme)
                    notifyManager.notify(Constants.DefaultNotifyID, notifyBuilder.build())
                }
                else -> {
                    notifyBuilder
                        .setSmallIcon(R.drawable.ic_indicator_cross)
                        .color = resources.getColor(R.color.colorDeclineBackground, theme)
                    notifyManager.notify(Constants.DefaultNotifyID, notifyBuilder.build())
                }
            }
        }

        val builder = NetworkRequest.Builder()
        val connectivityManager = getSystemService(Context.CONNECTIVITY_SERVICE) as ConnectivityManager
        connectivityManager.registerNetworkCallback(
            builder.build(),
            object : ConnectivityManager.NetworkCallback() {
                override fun onCapabilitiesChanged(network: Network, networkCapabilities: NetworkCapabilities) {
                    super.onCapabilitiesChanged(network, networkCapabilities)
                    sipStack.handleNetworkChange()
                }
            }
        )
    }

    override fun onStartCommand(intent: Intent?, flags: Int, startId: Int): Int {
        super.onStartCommand(intent, flags, startId)
        val action = intent?.action ?: Constants.IntentActions.Start.name
        when (action) {
            Constants.IntentActions.Start.name -> onStartService()
            Constants.IntentActions.Stop.name -> onStopService()
            Constants.IntentActions.StopForce.name -> onStopService()
            Constants.IntentActions.Kill.name -> onDestroy()
            Constants.IntentActions.HangupCall.name -> declineCurrentCall()
            Constants.IntentActions.OnIncomingCall.name -> incomingCall()
            Constants.IntentActions.AcceptIncomingCall.name -> acceptIncomingCall()
            Constants.IntentActions.DeclineIncomingCall.name -> declineIncomingCall()
            Constants.IntentActions.OutgoingCall.name -> outgoingCall()
            Constants.IntentActions.TransferCall.name -> transfer()
            Constants.IntentActions.DeclineTransferCall.name -> declineTransferCall()
        }
        return START_STICKY
    }

    override fun onDestroy() {
        super.onDestroy()
        stopForeground(true)
        sipStack.deInitLib()
    }

    override fun onTaskRemoved(rootIntent: Intent?) {
        super.onTaskRemoved(rootIntent)
        stopSelf()
    }

    private fun onStartService() {
        notifyManager = getSystemService(Service.NOTIFICATION_SERVICE) as NotificationManager
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
            NotificationChannel(defaultChannelID, "Default", NotificationManager.IMPORTANCE_LOW).also {
                it.lockscreenVisibility = Notification.VISIBILITY_PUBLIC
                it.setSound(null, null)
                notifyManager.createNotificationChannel(it)
            }
            notifyBuilder = NotificationCompat.Builder(this, defaultChannelID)
        } else {
            notifyBuilder = NotificationCompat.Builder(this).setPriority(NotificationCompat.PRIORITY_DEFAULT)
        }

        val pendingIntent = Intent(this, MainActivity::class.java).let {
            it.action = Constants.IntentActions.Start.name
            PendingIntent.getActivity(this, Constants.RequestCode.Default.ordinal, it, 0)
        }

        notifyBuilder
            .setVisibility(NotificationCompat.VISIBILITY_PUBLIC)
            .setOngoing(true)
//            .setUsesChronometer(true)
            .setContentTitle(notifyTitle)
            .setContentText(notifyText)
            .setContentIntent(pendingIntent)
            .setSmallIcon(R.drawable.ic_indicator_cross)
            .color = resources.getColor(R.color.colorDeclineBackground, theme)
        startForeground(Constants.DefaultNotifyID, notifyBuilder.build())

        partialWakeLock.acquire()
    }

    private fun onStopService() {
        if (audioManager.isBluetoothScoOn) audioManager.stopBluetoothSco()
        if (this::notifyManager.isInitialized) notifyManager.cancelAll()
        if (this::partialWakeLock.isInitialized && partialWakeLock.isHeld) partialWakeLock.release()
        if (this::proximityWakeLock.isInitialized && proximityWakeLock.isHeld) proximityWakeLock.release()
        if (this::wifiLock.isInitialized && wifiLock.isHeld) wifiLock.release()
        sipStack.deInitLib()
    }

    private fun incomingCall() {
        startRinging()

        val keyManager = getSystemService(KEYGUARD_SERVICE) as KeyguardManager
        if (keyManager.isKeyguardLocked) {
            Intent(this, MainActivity::class.java).let {
                it.action = Constants.IntentActions.OnIncomingCall.name
                it.flags = Intent.FLAG_ACTIVITY_NEW_TASK or
                        Intent.FLAG_ACTIVITY_SINGLE_TOP or
                        Intent.FLAG_ACTIVITY_CLEAR_TOP
                startActivity(it)
            }
        } else {
            val customView = RemoteViews(packageName, R.layout.call_notification).also {
                it.setTextViewText(R.id.callerName, viewModel.callName.value)
                it.setTextViewText(R.id.callerNumber, viewModel.callName.value)
                it.setTextViewText(R.id.callStatus, viewModel.callStatus.value)
//            it.setImageViewBitmap(R.id.photo, callerImage.bitmap)
            }

            val pendingIntent = Intent(this, MainActivity::class.java).let {
                it.action = Constants.IntentActions.OnIncomingCall.name
                it.flags = Intent.FLAG_ACTIVITY_NEW_TASK or
                        Intent.FLAG_ACTIVITY_SINGLE_TOP or
                        Intent.FLAG_ACTIVITY_CLEAR_TOP
                PendingIntent.getActivity(
                    this,
                    Constants.RequestCode.Call.ordinal,
                    it,
                    PendingIntent.FLAG_UPDATE_CURRENT
                )
            }

            val answerPendingIntent = Intent(this, EndlessService::class.java).let {
                it.action = Constants.IntentActions.AcceptIncomingCall.name
                PendingIntent.getService(
                    this,
                    Constants.RequestCode.Call.ordinal,
                    it,
                    PendingIntent.FLAG_UPDATE_CURRENT
                )
            }

            val rejectPendingIntent = Intent(this, EndlessService::class.java).let {
                it.action = Constants.IntentActions.DeclineIncomingCall.name
                PendingIntent.getService(
                    this,
                    Constants.RequestCode.Call.ordinal,
                    it,
                    PendingIntent.FLAG_UPDATE_CURRENT
                )
            }

            val notification = if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
                val notificationManager = getSystemService(Context.NOTIFICATION_SERVICE) as NotificationManager
                NotificationChannel(callChannelID, "IncomingCall", NotificationManager.IMPORTANCE_HIGH).also {
                    it.setSound(null, null)
                    notificationManager.createNotificationChannel(it)
                }

                customView.setOnClickPendingIntent(R.id.btnAnswer, answerPendingIntent)
                customView.setOnClickPendingIntent(R.id.btnDecline, rejectPendingIntent)

                NotificationCompat.Builder(this, callChannelID)
                    .setFullScreenIntent(pendingIntent, true)
                    .setStyle(NotificationCompat.DecoratedCustomViewStyle())
                    .setCustomContentView(customView)
                    .setCustomBigContentView(customView)
            } else {
                val answerAction = NotificationCompat
                    .Action
                    .Builder(R.drawable.ic_call_black_24dp, "HANG OUT", answerPendingIntent)
                    .build()
                val hangupAction = NotificationCompat
                    .Action
                    .Builder(R.drawable.ic_call_end_black_24dp, "HANG UP", rejectPendingIntent)
                    .build()
                NotificationCompat.Builder(this)
                    .setLargeIcon(BitmapFactory.decodeResource(this.resources, R.drawable.ic_call_black_24dp))
//                .setContentIntent(pendingIntent)
                    .setFullScreenIntent(pendingIntent, true)
                    .addAction(answerAction)
                    .addAction(hangupAction)
            }

            notification
                .setContentTitle(getString(R.string.app_name))
                .setTicker("Call_STATUS")
                .setContentText("IncomingCall")
                .setSmallIcon(R.drawable.ic_call_black_24dp)
                .setDefaults(Notification.DEFAULT_LIGHTS or Notification.DEFAULT_SOUND)
                .setVibrate(null)
                .setOngoing(true)
                .setPriority(NotificationCompat.PRIORITY_HIGH)
                .setCategory(NotificationCompat.CATEGORY_CALL)

            notifyManager.notify(Constants.CallNotifyID, notification.build())
        }
    }

    private fun acceptIncomingCall() {
        notifyManager.cancel(Constants.CallNotifyID)
        stopRinging()
        sipStack.acceptIncomingCall()
        viewModel.telephoneAnswer()
        viewModel.callNumber.postValue("")

        Intent(this, MainActivity::class.java).let {
            it.action = Constants.IntentActions.AcceptIncomingCall.name
            it.flags = Intent.FLAG_ACTIVITY_NEW_TASK
            startActivity(it)
        }
    }

    private fun declineIncomingCall() {
        notifyManager.cancel(Constants.CallNotifyID)
        stopRinging()
        sipStack.declineIncomingCall()
        viewModel.telephoneHangup()
        viewModel.callNumber.postValue("")
        Intent(this, MainActivity::class.java).let {
            it.action = Constants.IntentActions.DeclineIncomingCall.name
            it.flags = Intent.FLAG_ACTIVITY_NEW_TASK
            startActivity(it)
        }
    }

    private fun declineCurrentCall() {
        sipStack.declineCurrentCall()
        viewModel.callNumber.postValue("")
        Intent(this, MainActivity::class.java).let {
            it.action = Constants.IntentActions.DeclineIncomingCall.name
            it.flags = Intent.FLAG_ACTIVITY_NEW_TASK
            startActivity(it)
        }
    }

    private fun declineTransferCall() {
        sipStack.declineTransferCall()
        viewModel.callNumber.postValue("")
    }

    private fun outgoingCall() {
        val call = viewModel.telephoneCall.value
        viewModel.callNumber.value?.also {
            if (sipStack.registeredAccount.value.isNullOrEmpty()) {
                if (call == null || call.state == Call.STATE_DISCONNECTED) {
                    Intent(Intent.ACTION_CALL, "tel:${it}".toUri()).also { intent ->
                        intent.flags = Intent.FLAG_ACTIVITY_NEW_TASK
                        startActivity(intent)
                    }
                }
            } else {
//                sipStack.outgoingCall(it, accountViewModel.outgoingAccount.value?.get(0))
                sipStack.outgoingCall(it)
                viewModel.callNumber.postValue("")
            }
        }
    }

    private fun transfer() = if (isTransferReplace) transferReplaceCall() else transferCall()

    private fun transferCall() {
        viewModel.callNumber.value?.takeIf { it.isNotEmpty() }?.let {
            sipStack.transferCall(it)
            viewModel.callNumber.postValue("")
        } ?: return

        Intent(this, MainActivity::class.java).let {
            it.action = Constants.IntentActions.DeclineIncomingCall.name
            it.flags = Intent.FLAG_ACTIVITY_NEW_TASK
            startActivity(it)
        }
    }

    private fun transferReplaceCall() {
        viewModel.callNumber.value?.takeIf { it.isNotEmpty() }?.let {
            sipStack.transferCall(it)
            viewModel.callNumber.postValue("")
        } ?: return

        viewModel.isOnTransfer.postValue(true)

        Intent(this, MainActivity::class.java).let {
            it.action = Constants.IntentActions.AcceptIncomingCall.name
            it.flags = Intent.FLAG_ACTIVITY_NEW_TASK
            startActivity(it)
        }
    }

    private fun startRinging() {
        audioManager.mode = AudioManager.MODE_RINGTONE
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.P) {
            ringTone.isLooping = true
            ringTone.play()
        } else {
            ringTone.play()
            ringToneTimer = Timer()
            ringToneTimer?.scheduleAtFixedRate(object : TimerTask() {
                override fun run() {
                    if (!ringTone.isPlaying) {
                        ringTone.play()
                    }
                }
            }, 1000, 1000)
        }

        val timings = longArrayOf(0, 1000, 1000)
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
            val amplitudes = intArrayOf(0, VibrationEffect.DEFAULT_AMPLITUDE, 0)
            val effect = VibrationEffect.createWaveform(timings, amplitudes, 1)
            val audioAttrs = AudioAttributes.Builder()
                .setUsage(AudioAttributes.USAGE_NOTIFICATION_RINGTONE)
                .build()
            vibrator.vibrate(effect, audioAttrs)
        } else {
            vibrator.vibrate(timings, 1)
        }
    }

    private fun stopRinging() {
        if (audioManager.mode == AudioManager.MODE_RINGTONE) {
            if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.P) {
                ringTone.stop()
            } else if (ringToneTimer != null) {
                ringToneTimer?.cancel()
                ringToneTimer = null
            }
        }
        vibrator.cancel()
    }
